export type Translations = typeof t.es

function deepMerge<T extends object>(base: T, override: Partial<T>): T {
  const result = { ...base }
  for (const key in override) {
    const bv = base[key], ov = override[key]
    if (ov !== undefined) {
      ;(result as any)[key] =
        bv && typeof bv === 'object' && !Array.isArray(bv) && typeof ov === 'object' && !Array.isArray(ov)
          ? deepMerge(bv as object, ov as object)
          : ov
    }
  }
  return result
}

export function getTranslations(locale: string): Translations {
  const loc = (t as Record<string, unknown>)[locale]
  if (!loc) return t.es
  return deepMerge(t.es, loc as Partial<Translations>)
}
